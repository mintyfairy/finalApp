package com.fa.plus.admin.domain.site;

public class AdSite {
	private String startP;
	private String endP;
	private long adFeePerDay;
	private long adFee;
	private long siteNum;
	private long adNum;
	public String getStartP() {
		return startP;
	}
	public void setStartP(String startP) {
		this.startP = startP;
	}
	public String getEndP() {
		return endP;
	}
	public void setEndP(String endP) {
		this.endP = endP;
	}
	public long getAdFeePerDay() {
		return adFeePerDay;
	}
	public void setAdFeePerDay(long adFeePerDay) {
		this.adFeePerDay = adFeePerDay;
	}
	public long getAdFee() {
		return adFee;
	}
	public void setAdFee(long adFee) {
		this.adFee = adFee;
	}
	public long getSiteNum() {
		return siteNum;
	}
	public void setSiteNum(long siteNum) {
		this.siteNum = siteNum;
	}
	public long getAdNum() {
		return adNum;
	}
	public void setAdNum(long adNum) {
		this.adNum = adNum;
	}
	
	
}
